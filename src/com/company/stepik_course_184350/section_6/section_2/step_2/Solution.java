    private static int solve(int[] coords, int n, int r) {
        Arrays.sort(coords);
        int start = 0, end = n - 1, count = 0, pos = 0;

        for (int i = n - 1; i >= 0; i--) {
            if (coords[i] - coords[start] > r) {
                pos = i;
            }
        }

        while (end >= pos && start < end) {
            if (coords[end] - coords[start] > r) {
                count++;
            }

            if (start + 1 == end) {
                end--;
                start = 0;
            } else {
                start++;
            }
        }

        return count;
    }

